/*i) Read two numbers add them and display their sum
ALGORITHM
1. Start
2. Read a,b
3. sum=a+b
4. Print sum
5. Stop*/
#include<stdio.h>
void main()
{
int a,b,sum;
printf("Enter First Number:");
scanf("%d",&a);
printf("Enter Second Number:");
scanf("%d",&b);
sum=a+b;
printf("Sum of above numbers is:%d",sum);
}

